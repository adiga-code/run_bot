def main() -> None:
    workouts = json.loads(DATA_PATH.read_text(encoding="utf-8"))

    updated = 0
    not_found = []

    for w in workouts:
        key = (w["level"], w["day"], w["version"])
        if key in CHANGES:
            patch = CHANGES[key]
            for field, value in patch.items():
                w[field] = value
            updated += 1

    # Check for any keys that weren't matched
    found_keys = {(w["level"], w["day"], w["version"]) for w in workouts}
    for key in CHANGES:
        if key not in found_keys:
            not_found.append(key)

    DATA_PATH.write_text(
        json.dumps(workouts, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )

    print(f"Updated {updated} entries.")
    if not_found:
        print(f"WARNING — keys not found in workouts.json: {not_found}")
    else:
        print("All keys matched successfully.")


if __name__ == "__main__":
    main()
